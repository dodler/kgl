class AptosDS:
    def __init__(self, imgs, labels, aug):
        self.imgs = imgs
        self.labels = labels
        self.aug = aug
        self.to_tensor = transforms.ToTensor()
        self.normalize = transforms.Normalize(mean=[0.485, 0.456, 0.406],
                                              std=[0.229, 0.224, 0.225])

    def __len__(self):
        return len(self.imgs)

    def __getitem__(self, idx):
        img = self.imgs[idx]

        img = cv2.imread(img)
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        img = self.aug(image=img)['image']

        img = self.to_tensor(img)
        img = self.normalize(img)

        label = torch.as_tensor(self.labels[idx], dtype=torch.int64)

        return img, label

class ArcMarginProduct(nn.Module):
    r"""Implement of large margin arc distance: :
        Args:
            in_features: size of each input sample
            out_features: size of each output sample
            s: norm of input feature
            m: margin
            cos(theta + m)
        """
    def __init__(self, in_features, out_features, s=20.0, m=0.40, easy_margin=True):
        super(ArcMarginProduct, self).__init__()
        self.in_features = in_features
        self.out_features = out_features
        self.s = s
        self.m = m
        self.weight = Parameter(torch.FloatTensor(out_features, in_features))
        nn.init.xavier_uniform_(self.weight)

        self.easy_margin = easy_margin
        self.cos_m = math.cos(m)
        self.sin_m = math.sin(m)
        self.th = math.cos(math.pi - m)
        self.mm = math.sin(math.pi - m) * m

    def forward(self, input, label):
        # --------------------------- cos(theta) & phi(theta) ---------------------------
        cosine = F.linear(F.normalize(input), F.normalize(self.weight))
        sine = torch.sqrt(1.0 - torch.pow(cosine, 2))
        phi = cosine * self.cos_m - sine * self.sin_m
        if self.easy_margin:
            phi = torch.where(cosine > 0, phi, cosine)
        else:
            phi = torch.where(cosine > self.th, phi, cosine - self.mm)
        # --------------------------- convert label to one-hot ---------------------------
        # one_hot = torch.zeros(cosine.size(), requires_grad=True, device='cuda')
        one_hot = torch.zeros(cosine.size(), device='cuda')
        one_hot.scatter_(1, label.view(-1, 1).long(), 1)
        # -------------torch.where(out_i = {x_i if condition_i else y_i) -------------
        output = (one_hot * phi) + ((1.0 - one_hot) * cosine)  # you can use torch.where if your torch.__version__ is 0.4
        output *= self.s
        # print(output)

        return output


class ArcSEResnext50(nn.Module):
    def __init__(self, num_classes):
        super(ArcSEResnext50, self).__init__()
        self.feature_extr = se_resnext50_32x4d(pretrained='imagenet')
        self.feature_extr.load_state_dict(
            torch.load('../input/se-resnext-pytorch-pretrained/se_resnext50_32x4d-a260b3a4.pth'))
        self.pool = torch.nn.AdaptiveAvgPool2d((1, 1))
        self.metric = ArcMarginProduct(in_features=2048, out_features=num_classes)
        self.test = False

    def upd_metric(self, metric):
        self.metric = metric

    def set_test_mode(self, test):
        self.test = test

    def forward(self, x, labels):
        bs = x.shape[0]
        x = self.feature_extr.features(x)
        x = self.pool(x).reshape(bs, -1)

        if self.test:
            return x

        if labels is not None:
            return self.metric(x, labels)
        else:
            return self.metric(x)